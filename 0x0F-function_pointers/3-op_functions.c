#include <stdio.h>
#include <stdlib.h>

/**
 *op_add - function to add
 *@a: integer 1
 *@b: integer 2
 *Return: a + b
 */

int op_add(int a, int b)
{
    return (a + b);
}

/**
 *op_add - function to subtract
 *@a: integer 1
 *@b: integer 2
 *Return: a - b
 */

int op_sub(int a, int b)
{
    return (a - b);
}

/**
 *op_add - function to multiply
 *@a: integer 1
 *@b: integer 2
 *Return: a * b
 */

int op_mul(int a, int b)
{
    return (a * b);
}

/**
 *op_add - function to divide
 *@a: integer 1
 *@b: integer 2
 *Return: a / b
 */

int op_div(int a, int b)
{
    return (a / b);
}

/**
 *op_add - function to module
 *@a: integer 1
 *@b: integer 2
 *Return: a % b
 */

int op_mod(int a, int b)
{
    return (a % b);
}